import pandas as pd

df = pd.read_csv("Admission_Predict.csv")
df.drop(columns=['Serial No.'], inplace=True)
df.head()

X = df.drop('Chance of Admit ', axis=1)
y = df['Chance of Admit ']

from sklearn.model_selection import train_test_split
X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=10)

from sklearn.preprocessing import StandardScaler
sc = StandardScaler()

X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)

from sklearn.linear_model import LinearRegression
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import r2_score, mean_squared_error
import numpy as np

lr = LinearRegression()
lr.fit(X_train,y_train)
pred1 = lr.predict(X_test)

rf = RandomForestRegressor()
rf.fit(X_train,y_train)
pred2 = rf.predict(X_test)

print("Linear Regression R2:", r2_score(y_test,pred1))
print("Random Forest R2:", r2_score(y_test,pred2))

print("Linear RMSE:", np.sqrt(mean_squared_error(y_test,pred1)))
print("Random Forest RMSE:", np.sqrt(mean_squared_error(y_test,pred2)))